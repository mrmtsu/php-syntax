<?php
// シングルクォーテーションまたはダブルクォーテーションが必要
// function(パラメータ); という構文となる
// print 文字列を出力する
// print(string $arg) :int
// 終了タグ（?)は必要ない（省略できる）
print('PHPで出力しています');

// echo(string $arg1 [, string $...]) : void
// voidは何もない（値を返さない）
// echoはパラメータを複数とることができる
echo '/ echoで出力しています', '/ 2つめの文章です', '/ 3つめの文章です';

